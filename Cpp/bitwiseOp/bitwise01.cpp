#include <iostream>
using namespace std ;

int main (){
    // bitwise and , bitwise or and bitwise xor 
    cout << ( 6 & 10 )<<endl;
    cout << ( 6 | 10) <<endl;
    cout << ( 6 ^ 10) <<endl;

    //bitwise left shift and bitwise right shift 
    cout << (10 << 2) <<endl;
    cout << (10 >> 1) <<endl;
    return 0;
}